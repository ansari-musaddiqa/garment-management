package com.bean;

public class ColorBean {
	
	private int cid;
	private String cnm;
	private int active;
	
	public int getActive() 
	{
		return active;
	}

	public void setActive(int active) 
	{
		this.active = active;
	}

	public int getCid() 
	{
		return cid;
	}
	
	public void setCid(int cid) 
	{
		this.cid = cid;
	}
	
	public String getCnm()
	{
		return cnm;
	}
	
	public void setCnm(String cnm)
	{
		this.cnm = cnm;
	}
}
