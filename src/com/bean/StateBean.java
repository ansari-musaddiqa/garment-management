package com.bean;

public class StateBean {
	private int sid;
	private String snm;
	private int status;
	
	public int getSid() 
	{
		return sid;
	}
	
	public void setSid(int sid) 
	{
		this.sid = sid;
	}
	
	public String getSnm()
	{
		return snm;
	}
	
	public void setSnm(String snm) 
	{
		this.snm = snm;
	}
	
	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}
